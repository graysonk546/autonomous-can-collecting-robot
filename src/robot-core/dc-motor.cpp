static struct pt accelerometerThread;

/*******************************************************************************
*                               Variables
*******************************************************************************/

/*******************************************************************************
*                               Functions
*******************************************************************************/

robot_status_t accelerometer_init()
{
    PT_INIT(&accelerometerThread);
    return ROBOT_OK;
}

struct pt* accelerometer_getThread()
{
    return &accelerometerThread;
}