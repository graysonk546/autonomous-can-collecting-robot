cli_status_t cliSonar_init(uint8_t argNumber, char* args[])
{
    if (sonar_init() != ROBOT_OK)
    {
        Serial.print(F(CMD_JSON "{\"status\": \"error\", \"data\": \"failed"
                     " to init sonar\"}" CMD_EOL_STR));
    }
    Serial.print(F(CMD_JSON "{\"status\": \"success\"}" CMD_EOL_STR));
    return COMMAND_OK;
}

