typedef enum {
    COMMAND_OK,
    COMMAND_ERROR
} cli_status_t;

typedef cli_status_t (* cli_func_t)(uint8_t argNumber, char* args[]);

typedef struct {
    cli_func_t function;
    const char *command;
    const char *parameters;
    const char *description;
    uint8_t minParam;
    uint8_t maxParam;
} command_t;

/*******************************************************************************
*                               Variables
*******************************************************************************/

/*******************************************************************************
*                               Functions
*******************************************************************************/

cli_status_t command_help(uint8_t argNumber, char* args[]);

#define COMMAND_COMMANDS                                                       \
    {command_help, "help", "", "lists all supported commands", 0, 0},

