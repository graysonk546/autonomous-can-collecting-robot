cli_status_t command_help(uint8_t argNumber, char* args[])
{
    uint8_t i = 0;
    while (strcmp(commandArr[i].command, LIST_TERMINATOR) != 0)
    {
        Serial.print(commandArr[i].command);
        if (strcmp(commandArr[i].parameters, "") != 0)
        {
            Serial.print(F(" "));
            Serial.print(commandArr[i].parameters);
        }
        Serial.print(F(" - "));
        Serial.print(commandArr[i].description);
        Serial.println(F(""));
        i++;
    }
    return COMMAND_OK;
}